---------------------------------------------------------------------------

CREATE TABLE Player (
    player_id INT,
    f_name VARCHAR(50) NOT NULL,
    l_name VARCHAR(50) NOT NULL,
    wins INT DEFAULT 0,
    losses INT DEFAULT 0,
    dom_hand CHAR(1),
    total_prize_money DECIMAL(11,2) DEFAULT 0,
    PRIMARY KEY (player_id)
);

CREATE TABLE Tournament (
    tourney_id INT,
    name VARCHAR NOT NULL,
    location VARCHAR(100) NOT NULL,
    surface VARCHAR(5) NOT NULL,
    category VARCHAR(50) NOT NULL,
    total_prize_money DECIMAL(10,2) NOT NULL,
    start_month CHAR(7) NOT NULL,
    end_month CHAR(7) NOT NULL,
    PRIMARY KEY (tourney_id),
    CONSTRAINT surface_check CHECK (surface IN ('Hard', 'Clay', 'Grass')),
    CONSTRAINT category_check CHECK (category IN ('Grand Slam', 'ATP 1000', 'ATP 500', 'ATP 250', 'Challenger', 'Exhibition'))
);

CREATE TABLE Ranking (
    rank_id INT,
    player_id INT NOT NULL,
    rank INT NOT NULL,
    points INT NOT NULL,
    date DATE NOT NULL,
    PRIMARY KEY (rank_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

CREATE TABLE Match (
    match_id INT,
    tourney_id INT NOT NULL,
    player1_id INT NOT NULL,
    player2_id INT NOT NULL,
    round VARCHAR(50) NOT NULL,
    score VARCHAR(50) NOT NULL,
    match_date DATE NOT NULL,
    winner_id INT NOT NULL,
    PRIMARY KEY (match_id),
    FOREIGN KEY (tourney_id) REFERENCES Tournament(tourney_id),
    FOREIGN KEY (player1_id) REFERENCES Player(player_id),
    FOREIGN KEY (player2_id) REFERENCES Player(player_id),
    FOREIGN KEY (winner_id) REFERENCES Player(player_id),
    CONSTRAINT tourney_round_check CHECK (round IN ('Final', 'Semi-Final', 'Quarter-Final', 'Round of 16', 'Round of 32', 'Round of 64', 'Round Robin'))
);

CREATE TABLE Coach (
    coach_id INT,
    f_name VARCHAR(50) NOT NULL,
    l_name VARCHAR(50) NOT NULL,
    nationality VARCHAR(50),
    player_id INT,
    PRIMARY KEY (coach_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

CREATE TABLE Player_Stats (
    stats_id INT,
    player_id INT NOT NULL,
    aces INT DEFAULT 0,
    double_faults INT DEFAULT 0,
    first_serve_percentage DECIMAL DEFAULT 0,
    second_serve_percentage DECIMAL DEFAULT 0,
    break_points_saved INT DEFAULT 0,
    break_points_converted INT DEFAULT 0,
    total_matches_played INT DEFAULT 0,
    PRIMARY KEY (stats_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

CREATE TABLE Sponsor (
    name VARCHAR(100),
    player_id INT,
    budget DECIMAL(12,2) NOT NULL,
    PRIMARY KEY (name, player_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

CREATE TABLE Injury (
    injury_id INT,
    player_id INT NOT NULL,
    injury_type VARCHAR(200),
    start_date DATE NOT NULL,
    end_date DATE,
    PRIMARY KEY (injury_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

CREATE TABLE Match_Stats (
    match_id INT,
    player_id INT,
    aces INT DEFAULT 0,
    double_faults INT DEFAULT 0,
    winners INT DEFAULT 0,
    unforced_errors INT DEFAULT 0,
    total_points_won INT DEFAULT 0,
    PRIMARY KEY (match_id, player_id),
    FOREIGN KEY (match_id) REFERENCES Match(match_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

CREATE TABLE Country (
    name VARCHAR(100),
    continent VARCHAR(50) NOT NULL,
    PRIMARY KEY (name)
);

CREATE TABLE Tourney_Winners (
    tourney_id INT,
    year INT,
    player_id INT NOT NULL,
    PRIMARY KEY (tourney_id, year),
    FOREIGN KEY (tourney_id) REFERENCES Tournament(tourney_id),
    FOREIGN KEY (player_id) REFERENCES Player(player_id)
);

